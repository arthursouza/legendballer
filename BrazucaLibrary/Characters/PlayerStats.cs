namespace BrazucaLibrary.Characters
{
    public class Stats
    {
        public Stats(int age, int fame, int power, int technique)
        {
            Age = age;
            Fame = fame;
            Power = power;
            Technique = technique;
        }

        public Stats()
        {
        }

        public int Age;
        public int Fame;
        public int Power;
        public int Technique;

    }
}
